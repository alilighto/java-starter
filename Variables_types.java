public class Variables_types {
    public static void main(String[] args) {
        char H = 'H';
        byte three = 3;
        short one = 1;
        int zero = 0;
        float two = 2.0f;
        boolean bool = true;

        String output = "" + H + three + one + one + zero + " w" + zero + "r" + one + "d " + two + " " + bool;
        System.out.println(output);
    }
}
